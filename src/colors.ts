export const RGB = tailwindColors.RGB;
export const HEX = tailwindColors.HEX;

export type TailwindColorFormat = 'HEX' | 'RGB';

export type TailwindColorName = keyof typeof HEX;

export type TailwindSimpleColorName = Extract<TailwindColorName, 'black' | 'white'>;

export type TailwindColorPaletteName = Exclude<TailwindColorName, TailwindSimpleColorName>;

export type TailwindColorShadeNumber = 50 | 100 | 200 | 300 | 400 | 500 | 600 | 700 | 800 | 900 | 950;

export type TailwindColorShade = `${TailwindColorShadeNumber}`;

export type TailwindHexPalette = typeof HEX[TailwindColorPaletteName];

export type TailwindRgbPalette = typeof RGB[TailwindColorPaletteName];

export type TailwindHexColorValue = string;

export type TailwindRgbColorValue = { r: number; g: number; b: number };

export type GetHexColor<
  Name extends TailwindColorPaletteName,
  Shade extends TailwindColorShade
> = typeof HEX[Name][Shade & keyof typeof HEX[Name]];

export type GetRgbColor<
  Name extends TailwindColorPaletteName,
  Shade extends TailwindColorShade
> = typeof RGB[Name][Shade & keyof typeof RGB[Name]];

export type GetSimpleHexColor<Name extends TailwindSimpleColorName> = typeof HEX[Name];

export type GetSimpleRgbColor<Name extends TailwindSimpleColorName> = typeof RGB[Name];